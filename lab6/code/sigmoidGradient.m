function g = sigmoidGradient(z)
%SIGMOIDGRADIENT возвращает градиент сигмоиды в точке z

g = zeros(size(z));

% ====================== НАЧАЛО ВАШЕГО КОДА ======================
% Подсказка: z может быть скаляром, вектором или матрицей
%

sig = 1.0 ./ (1.0 + exp(-z));
g = sig .* (1 - sig);

% ====================== КОНЕЦ ВАШЕГО КОДА ======================

end
